/**
 * Returns a translated display name for a disease or a comma-separated list of diseases.
 * @param name - The English name or code of the disease(s).
 * @param lang - The target language ('en' or 'zh').
 * @returns The translated string.
 */
export const getDiseaseDisplayName = (name: string, lang: Language): string => {
  if (!name) return lang === 'zh' ? '正常' : 'Normal';
  
  // Handle comma separated lists
  const names = name.split(',').map(n => n.trim());
  const translated = names.map(n => {
    const dict = (translations[lang] as any);
    return dict[n] || n;
  });
  
  return translated.join(', ');
};
